async function main() {
    const FABRIC_GROUP = 4;

    // Remove any existing Budget rows first
    const deleted = await prisma.pricingMatrix.deleteMany({ where: { fabricGroup: FABRIC_GROUP } });
    console.log(`Deleted ${deleted.count} existing Budget (group 4) pricing rows`);

    const rows: { fabricGroup: number; width: number; drop: number; price: number }[] = [];
    for (const [widthStr, drops] of Object.entries(BUDGET_DATA)) {
        const width = parseInt(widthStr);
        for (const [dropStr, price] of Object.entries(drops)) {
            rows.push({ fabricGroup: FABRIC_GROUP, width, drop: parseInt(dropStr), price });
        }
    }

    await prisma.pricingMatrix.createMany({ data: rows });
    console.log(`Inserted ${rows.length} Budget pricing rows (group 4)`);
}

main()
